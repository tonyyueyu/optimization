def sync_uploads_from_gcs(session_id: str, local_upload_dir: str):
    """Download files from GCS to local so the kernel can access them."""
    client = get_gcs_client()
    if not client or not BUCKET_NAME:
        return

    try:
        bucket = client.bucket(BUCKET_NAME)
        prefix = f"uploads/{session_id}/"
        blobs = list(bucket.list_blobs(prefix=prefix))

        for blob in blobs:
            # Extract filename from the GCS path
            filename = os.path.basename(blob.name)
            if not filename: continue

            local_path = os.path.join(local_upload_dir, filename)
            
            # Only download if it doesn't exist or size is different
            if not os.path.exists(local_path) or os.path.getsize(local_path) != blob.size:
                blob.download_to_filename(local_path)
                print(f"  ↓ Synced from GCS: {filename}")
    except Exception as e:
        print(f"WARNING: Sync from GCS failed: {e}")


def upload_exports_to_gcs(session_id: str, export_dir: str) -> List[Dict]:
    """Upload files generated by the code (exports/) to GCS."""
    client = get_gcs_client()
    if not os.path.exists(export_dir): return []

    files_found = [f for f in glob.glob(f"{export_dir}/**/*", recursive=True) if os.path.isfile(f)]
    exported = []

    for filepath in files_found:
        filename = os.path.basename(filepath)
        run_id = uuid.uuid4().hex[:6]
        gcs_path = f"outputs/{session_id}/{run_id}_{filename}"
        
        if client and BUCKET_NAME:
            try:
                bucket = client.bucket(BUCKET_NAME)
                blob = bucket.blob(gcs_path)
                blob.upload_from_filename(filepath)
                exported.append({"name": filename, "gcs_path": gcs_path, "url": f"https://storage.googleapis.com/{BUCKET_NAME}/{gcs_path}"})
            except Exception as e:
                print(f"Export failed for {filename}: {e}")
        else:
            exported.append({"name": filename, "gcs_path": "local_only"})
            
    return exported

