class CodeRequest(BaseModel):
    code: str
    session_id: str
    timeout: int = 120


@app.post("/upload")
async def upload_file(file: UploadFile = File(...), session_id: str = Form(...)):
    """Saves file to LOCAL uploads folder AND GCS bucket."""
    if not file.filename:
        raise HTTPException(status_code=400, detail="No filename provided")

    session_root, upload_dir, _ = get_session_paths(session_id)
    local_path = os.path.join(upload_dir, file.filename)

    # 1. Save Locally
    try:
        content = await file.read()
        with open(local_path, "wb") as f:
            f.write(content)
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Failed to save locally: {e}")

    # 2. Upload to GCS (Background-ish)
    client = get_gcs_client()
    gcs_status = "skipped"
    if client and BUCKET_NAME:
        try:
            bucket = client.bucket(BUCKET_NAME)
            blob = bucket.blob(f"uploads/{session_id}/{file.filename}")
            blob.upload_from_filename(local_path)
            gcs_status = "success"
        except Exception as e:
            print(f"GCS Upload Error: {e}")
            gcs_status = f"error: {e}"

    return {
        "status": "success",
        "filename": file.filename,
        "local_path": local_path,
        "gcs": gcs_status
    }


@app.post("/execute")
async def execute(request: CodeRequest):
    session_root, upload_dir, export_dir = get_session_paths(request.session_id)

    # 1. Sync files from GCS (in case they were uploaded to a different instance)
    sync_uploads_from_gcs(request.session_id, upload_dir)

    # 2. Prepare Kernel
    if request.session_id not in kernels:
        if PersistentKernel is None:
            raise HTTPException(status_code=500, detail="Kernel manager unavailable")
        kernels[request.session_id] = PersistentKernel()

    kernel = kernels[request.session_id]

    # 3. Setup environment inside the kernel
    # We set the CWD to session_root and add 'uploads' to the path
    setup_code = f"""
import os, sys
os.chdir(r'{os.path.abspath(session_root)}')
if 'uploads' not in sys.path: sys.path.append(r'{os.path.abspath(upload_dir)}')
"""
    kernel.execute_code(setup_code)

    # 4. Clear old exports
    for f in os.listdir(export_dir):
        shutil.rmtree(os.path.join(export_dir, f), ignore_errors=True) if os.path.isdir(os.path.join(export_dir, f)) else os.unlink(os.path.join(export_dir, f))

    # 5. Run User Code
    loop = asyncio.get_running_loop()
    try:
        result = await asyncio.wait_for(
            loop.run_in_executor(None, kernel.execute_code, request.code),
            timeout=request.timeout
        )
        
        # 6. Export files
        result["files"] = upload_exports_to_gcs(request.session_id, export_dir)
        return result

    except asyncio.TimeoutError:
        return {"status": "error", "error": "Timeout"}
    except Exception as e:
        return {"status": "error", "error": str(e)}

